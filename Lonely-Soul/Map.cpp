Map::Map(){
    dirt = TextureManger::LoadTexture("Assets/Images/dirt.png");
    grass = TextureManger::LoadTexture("Assets/Images/grass.png");
    water = TextureManger::LoadTexture("Assets/Images/water.png");

    loadMap(lvl1);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;

    dest.x = dest.y = 0;
}

void Map::loadMap(int arr[20][25]){
    for(int row = 0; row < 20; row++){
        for(int column = 0; column < 25; column++){
            map[row][column] = arr[row][column];
        }
    }
}

void Map::drawMap(){
    int type = 0;

    for(int row = 0; row < 20; row++){
        for(int column = 0; column < 25; column++){
            type = map[row][column];

            dest.x = column * 32;
            dest.y = row * 32;
            switch(type){
                case 0: TextureManger::Draw(water, src, dest); break;
                case 1: TextureManger::Draw(grass, src, dest); break;
                case 2: TextureManger::Draw(dirt, src, dest); break;
                default:
                    break;
            }
        }
    }
}

int Map::GetTile(int x, int y) {
    // Chuyển vị trí pixel (x, y) thành chỉ số ô (row, col)
    int row = y / 32;  // Mỗi ô cao 32 pixel
    int col = x / 32;  // Mỗi ô rộng 32 pixel

    // Kiểm tra giới hạn của bản đồ
    if (row >= 0 && row < 20 && col >= 0 && col < 25) {
        return map[row][col];
    }
    return 0;  // Nếu ngoài bản đồ, coi như là nước (không đi được)
}

Map::~Map(){
    SDL_DestroyTexture(dirt);
    SDL_DestroyTexture(grass);
    SDL_DestroyTexture(water);
}
